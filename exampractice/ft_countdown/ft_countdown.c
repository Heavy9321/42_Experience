#include <unistd.h>

int	ft_countdown(char b)
{
	b = '9';
	while(b >= '0')
	{
		write(1, &b, 1);
		b--;
	}
	write(1, "\n", 1);
	return(b);
}

int main()
{
	ft_countdown('b');
}
